#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int i;

	if (s && f)
	{
		i = ft_strlen(s);
		while (i--)
		{
			f(i, &s[i]);
		}
	}
}
