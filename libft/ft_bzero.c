#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	char			*pos;

	i = 0;
	pos = (char *)s;
	while (i < n)
	{
		*pos = 0;
		pos++;
		i++;
	}
}
