#include "libft.h"

void ft_putnbr(long int n)
{
	if (n == LONG_MIN)
	{
		ft_putstr("-9223372036854775808");
	}
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		if (n < 10)
		{
			ft_putchar(n + 48);
		}
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
	}
}
