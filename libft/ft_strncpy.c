#include "libft.h"

char *ft_strncpy(char *dst, const char *src, size_t n)
{
	unsigned int i;
	unsigned int len;

	i = 0;
	len = ft_strlen(src);
	while (i < len && i < n)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
