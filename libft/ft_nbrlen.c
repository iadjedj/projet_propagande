#include "libft.h"

int		ft_nbrlen(intmax_t nb)
{
	int i;

	i = 1;
	if (nb == LLONG_MIN)
		return (20);
	if (nb < 0)
	{
		i++;
		nb = -nb;
	}
	while (nb >= 10)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
